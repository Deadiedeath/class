package member;

public class Member {
	private String name;
	private String id;
	private String ps;
	private String ps_check;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPs() {
		return ps;
	}
	public void setPs(String ps) {
		this.ps = ps;
	}
	public String getPs_check() {
		return ps_check;
	}
	public void setPs_check(String ps_check) {
		this.ps_check = ps_check;
	}
	
}
